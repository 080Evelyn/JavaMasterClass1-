package day1;

public class Measurement {
	
	private int length;
	private int breadth;
	
	public Measurement(int length, int breadth) {
		this.length = length;
		this.breadth = breadth;
	}

	public int getLength() {
		return length;
	}

	public int getBreadth() {
		return breadth;
	}
	
	
	
	

}
